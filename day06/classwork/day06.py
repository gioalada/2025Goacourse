b = 3
sum_result = a + b
print(f"{a} + {b} = {sum_result}")

# - (დაკლება)
a = 10
b = 4
difference_result = a - b
print(f"{a} - {b} = {difference_result}")

# * (გამრავლება)
a = 6
b = 7
multiplication_result = a * b
print(f"{a} * {b} = {multiplication_result}")

# / (გაყოფა)
a = 20
b = 4
division_result = a / b
print(f"{a} / {b} = {division_result}")



# მშობლების ასაკები
father_age = 41  # მამა
mother_age = 39  # დედა

# 20 წლის შემდგომი ასაკები
father_age_in_20_years = father_age + 20
mother_age_in_20_years = mother_age + 20

# შედეგის დაბეჭდვა
print(f"მამა 20 წლის შემდეგ იქნება {father_age_in_20_years} წლის.")
print(f"დედა 20 წლის შემდეგ იქნება {mother_age_in_20_years} წლის.")
